    /**
     * A placeholder fragment containing a simple view.
     */

    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            // getItem is called to instantiate the fragment for the given page.
            // Return a PlaceholderFragment (defined as a static inner class below).
            System.out.println("position: " + position);
            switch (position) {
                case 0:
                    return PlayersFragment.newInstance(position + 1, game_logic);
                case 1:
                    return TeamsFragment.newInstance(position + 1, game_logic);
                case 2:
                    return GameFragment.newInstance(position + 1, game_logic);
                case 3:
                    return StatsFragment.newInstance(position + 1, game_logic);
                default:
                    System.out.println("default getItem case");
                    return PlayersFragment.newInstance(position + 1, game_logic);
            }
            //return PlayersFragment.newInstance(position + 1);
        }

        @Override
        public int getCount() {
            // Show 3 total pages.
            return 4;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "Players";
                case 1:
                    return "Teams";
                case 2:
                    return "Game";
                case 3:
                    return "Stats";
            }
            return null;
        }
    }
